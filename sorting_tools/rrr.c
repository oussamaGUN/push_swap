#include "../main.h"

void	rrr(t_list **a, t_list **b, char c)
{
	if (c != 'd' && c != 'c')
	{
		*a = rra(*a, 'h');
		*b = rra(*b, 'h');
	}
	else
	{
		*a = rra(*a, 'd');
		*b = rra(*b, 'c');
	}
}

void	rr(t_list **a, t_list **b, char c)
{
	if (c != 'd' && c != 'c')
	{
		*a = ra(*a, 'h');
		*b = ra(*b, 'h');
	}
	else
	{
		*a = ra(*a, 'd');
		*b = ra(*b, 'c');
	}
}
